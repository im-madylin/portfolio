import { promises as fs } from "node:fs";
import path from "node:path";

const rootDir = process.cwd();
const projectsDir = path.join(rootDir, "public", "projects");
const outputFilePath = path.join(rootDir, "lib", "generated-project-images.ts");
const allowedExtensions = new Set([".webp", ".jpg", ".jpeg", ".png", ".svg", ".avif"]);

const sortNaturally = (left, right) =>
  left.localeCompare(right, undefined, { numeric: true, sensitivity: "base" });

const findProjectImages = async () => {
  let projectFolders = [];

  try {
    const entries = await fs.readdir(projectsDir, { withFileTypes: true });
    projectFolders = entries
      .filter((entry) => entry.isDirectory())
      .map((entry) => entry.name)
      .sort(sortNaturally);
  } catch (error) {
    if (error && typeof error === "object" && "code" in error && error.code === "ENOENT") {
      return {};
    }

    throw error;
  }

  const result = {};

  for (const folderName of projectFolders) {
    const folderPath = path.join(projectsDir, folderName);
    const entries = await fs.readdir(folderPath, { withFileTypes: true });

    const imagePaths = entries
      .filter((entry) => entry.isFile())
      .map((entry) => entry.name)
      .filter((fileName) => allowedExtensions.has(path.extname(fileName).toLowerCase()))
      .sort(sortNaturally)
      .map((fileName) => `/projects/${folderName}/${fileName}`);

    if (imagePaths.length > 0) {
      result[folderName] = imagePaths;
    }
  }

  return result;
};

const writeGeneratedFile = async (imageMap) => {
  const fileContent = `// AUTO-GENERATED by scripts/generate-project-images.mjs\n// Do not edit this file directly.\n\nexport const generatedProjectImages: Record<string, string[]> = ${JSON.stringify(imageMap, null, 2)};\n`;

  await fs.writeFile(outputFilePath, fileContent, "utf8");
};

const imageMap = await findProjectImages();
await writeGeneratedFile(imageMap);
console.log(`Generated ${path.relative(rootDir, outputFilePath)}`);
